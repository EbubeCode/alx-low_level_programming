/**
 * op_add - adds two numbers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of the two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - finds the difference b/w two numbers
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of the two integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - finds the product of two numbers
 * @a: first integer
 * @b: second integer
 *
 * Return: product of the two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides a by b
 * @a: first integer
 * @b: second integer
 *
 * Return: quotient of division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - get a % b
 * @a: first integer
 * @b: second integer
 *
 * Return: modulus
 */
int op_mod(int a, int b)
{
	return (a % b);
}
